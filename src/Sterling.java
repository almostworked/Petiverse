/**
 * The Sterling class represents a pet character named Sterling.
 * It extends the Pet class and initializes Sterling with specific attributes.
 * 
 * author Adrian
 * @version 1.0
 */
public class Sterling extends Pet {

    /**
     * Constructs a new Sterling pet with the given name.
     *
     * @param name the name of the pet
     */
    public Sterling(String name) {
        super(name, 95, 90, 100, 75, true, "NORMAL");
    }
}